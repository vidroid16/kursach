package com.example.demo;

public class AdamsBody {
    private String f;
    private double x0;
    private double y0;
    private double xn;
    private double eps;


    public String getF() {
        return f;
    }

    public void setF(String f) {
        this.f = f;
    }

    public double getX0() {
        return x0;
    }

    public void setX0(double x0) {
        this.x0 = x0;
    }

    public double getY0() {
        return y0;
    }

    public void setY0(double y0) {
        this.y0 = y0;
    }

    public double getXn() {
        return xn;
    }

    public void setXn(double xn) {
        this.xn = xn;
    }

    public double getEps() {
        return eps;
    }

    public void setEps(double eps) {
        this.eps = eps;
    }
}
