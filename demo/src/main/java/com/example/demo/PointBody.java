package com.example.demo;

public class PointBody {
    private double x;
    private String f;
    private String l;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public String getF() {
        return f;
    }

    public void setF(String f) {
        this.f = f;
    }

    public String getL() {
        return l;
    }

    public void setL(String l) {
        this.l = l;
    }
}
